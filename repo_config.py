def config_func_to_str() -> str:
    """
    Print the value of all the config functions.
    """
    ret: List[str] = []
    for func_name in sorted([
        "get_docker_base_image_name",
        "get_docker_user",
        "get_host_name",
        "get_invalid_words",
        "get_name",
        "get_repo_map",
        "has_dind_support",
        "is_AM_S3_available",
        "is_CK_S3_available",
        "is_dev_ck",
        "is_dev4",
        "is_inside_ci",
        "is_inside_docker",
        "is_mac",
        "run_docker_as_root",
        "use_docker_shared_cache",
        "use_docker_sibling_containers",
        "use_docker_network_mode_host",
    ]):
        try:
            _LOG.debug("func_name=%s", func_name)
            func_value = eval("%s()" % func_name)
        except NameError:
            func_value = "*undef*"
        msg = "%s='%s'" % (func_name, func_value)
        ret.append(msg)
        # _print(msg)
    ret = "\n".join(ret)
    return ret


